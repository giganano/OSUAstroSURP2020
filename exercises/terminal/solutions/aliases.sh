# The following line will create the alias 'lc' which I use in all of my 
# computers to expand on the 'ls' command. The -lha flags cause it to also 
# show the read/write/execute privileges, whether or not one of the contents 
# is a directory, the size of the files, and date and time modified. 

alias lc="ls -lha" 

# This change will be permanent as long as this line remains in the file you 
# put it into. To get rid of it, simply delete the line and restart your 
# terminal. 

